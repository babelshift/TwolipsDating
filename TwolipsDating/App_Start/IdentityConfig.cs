using Microsoft.AspNet.Identity;
using Microsoft.AspNet.Identity.EntityFramework;
using Microsoft.AspNet.Identity.Owin;
using Microsoft.Owin;
using Microsoft.Owin.Security;
using SendGrid;
using System;
using System.Configuration;
using System.Net;
using System.Net.Mail;
using System.Security.Claims;
using System.Threading.Tasks;
using TwolipsDating.Models;

namespace TwolipsDating
{
    public class EmailService : IIdentityMessageService
    {
        public Task SendAsync(IdentityMessage message)
        {
            if(message.Destination == "disabled@disabled.com")
            {
                return Task.FromResult(0);
            }

            SendGridMessage sgMessage = new SendGridMessage();
            sgMessage.From = new MailAddress("noreply@twolipsdating.com", "Twolips Dating team");
            sgMessage.AddTo(message.Destination);
            sgMessage.Subject = message.Subject;
            sgMessage.Html = message.Body;
            sgMessage.Text = message.Body;

            sgMessage.EnableTemplateEngine("bae59d00-659c-4617-817f-d96e83061d8b");

            string username = ConfigurationManager.AppSettings["sendGridUserName"];
            string password = ConfigurationManager.AppSettings["sendGridPassword"];

            NetworkCredential credential = new NetworkCredential(username, password);

            Web transport = new Web(credential);

            if (transport != null)
            {
                return transport.DeliverAsync(sgMessage);
            }
            else
            {
                return Task.FromResult(0);
            }
        }
    }

